// B-011 themes catalog. 6 themes × 15 sub-scenes = 90 background nodes with
// a directed graph (connects_to) for geographic adjacency. Backs the new
// theme-driven landing flow:
//
// - Kid picks a theme on the landing page.
// - Kid picks (or types) an entry sub-scene from that theme's grid.
// - The /api/generate route validates the picks against this catalog and
//   passes the picked theme's full sub-scene list to lib/claude.ts as
//   context.
// - Claude builds a story tree where scene 1 == entry, scenes 2-4 follow
//   library connects_to edges from the previous scene, scenes 5+ may use
//   any sub-scene in the same theme, and ending scenes use sub-scenes
//   flagged can_be_ending.
// - The renderer resolves scene.background_id via SUB_SCENES_BY_ID[id]
//   .file_path. Old worlds (theme = null in DB) keep falling through to the
//   B-010 matcher path.
//
// String ids + runtime validation, no literal unions. validateThemeCatalog
// (in lib/catalog-validator.ts) runs at module load and throws on init if
// any cross-reference is broken.

import { validateThemeCatalog } from "@/lib/catalog-validator";

export type SubScene = {
  id: string;
  theme: string;
  label: string;
  description: string;
  file_path: string;
  connects_to: string[];
  can_be_entry: boolean;
  can_be_ending: boolean;
  // B-013 pilot: optional entry video that plays once on first scene entry
  // per (world_id, scene_id), then crossfades to file_path. Drawbridge only.
  entry_video_path?: string;
  // B-012 gates: pickup ids the player must hold before the gating choice
  // unlocks. Validated against lib/pickups-catalog at module load. Claude
  // reads this list and emits matching `requires` arrays on the choice
  // leading INTO this sub-scene. Empty/undefined means no gate.
  required_pickups?: string[];
  // B-021 demo polish: when true, the sub-scene tile renders locked in the
  // landing form's "where do you start" picker. Used to hide every castle
  // entry except the dragon-egg quest tile for the demo.
  locked?: boolean;
};

export type Theme = {
  id: string;
  label: string;
  description: string;
  thumbnail_path: string;
  sub_scenes: SubScene[];
  // B-021 demo polish: when true, the theme tile renders grayscale with a
  // 🔒 overlay and is not selectable. Castle is the only unlocked theme for
  // the hackathon demo; other themes still appear in the catalog for the
  // future build-out.
  locked?: boolean;
};

