export const THEMES_BY_ID: Record<string, Theme> = Object.fromEntries(
  THEMES.map((t) => [t.id, t])
);

export const SUB_SCENES_BY_ID: Record<string, SubScene> = Object.fromEntries(
  THEMES.flatMap((t) => t.sub_scenes.map((s) => [s.id, s] as const))
);

export const ALL_THEME_IDS: Set<string> = new Set(THEMES.map((t) => t.id));

export function getTheme(themeId: string): Theme | null {
  return THEMES_BY_ID[themeId] ?? null;
}

export function getSubScene(subSceneId: string): SubScene | null {
  return SUB_SCENES_BY_ID[subSceneId] ?? null;
}

export function isValidThemeId(id: string): boolean {
  return ALL_THEME_IDS.has(id);
}

export function isValidSubSceneId(id: string): boolean {
  return id in SUB_SCENES_BY_ID;
}

export function subScenesForTheme(themeId: string): SubScene[] {
  return THEMES_BY_ID[themeId]?.sub_scenes ?? [];
}
